public class ArithmeticOperations{
    public static void main(String[] args){
        double x = 10.5;
        double y = 6.9;
        double add = x + y;
        double subtract = x - y;
        double multiply = x * y;
        double divide = x / y;
        System.out.println(add + "\n" + subtract + "\n" + multiply + "\n" + divide);
    }
}